import 'package:flutter/material.dart';

import '../../../../features/utility/const/constant_color.dart';
import '../../../../features/utility/const/constant_string.dart';
import '../../../../features/utility/extension/text_theme_extension.dart';
import '../../../../features/utility/extension/color_extension.dart';

class AppointmentCard extends StatelessWidget {
  final String departmentName;
  final String branchName;
  final String appointmentTime;
  final String doctorName;
  final String appointmentID;
  final String guid;
  final VoidCallback? onTap;
  final VoidCallback? onCancel;
  final bool isRegisterable;

  const AppointmentCard({
    super.key,
    required this.departmentName,
    required this.appointmentTime,
    required this.doctorName,
    required this.branchName,
    required this.appointmentID,
    required this.guid,
    this.onTap,
    this.onCancel,
    this.isRegisterable = false,
  });

  @override
  Widget build(BuildContext context) {
    return InkWell(
      onTap: onTap,
      borderRadius: BorderRadius.circular(16),
      hoverColor: context.primaryColor.withOpacity(0.05),
      splashColor: context.primaryColor.withOpacity(0.1),
      highlightColor: context.primaryColor.withOpacity(0.05),
      child: Container(
        decoration: BoxDecoration(
          color: ConstColor.white,
          borderRadius: BorderRadius.circular(16),
          border: Border(
            left: BorderSide(color: context.primaryColor, width: 8),
          ),
          boxShadow: [
            BoxShadow(
              // ignore: deprecated_member_use
              color: ConstColor.black.withOpacity(0.05),
              blurRadius: 10,
              offset: const Offset(0, 4),
            ),
          ],
        ),
        child: Padding(
          padding: const EdgeInsets.all(24.0),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    branchName,
                    style: context.sectionTitle.copyWith(fontSize: 24),
                  ),
                  Icon(
                    Icons.arrow_forward_ios_rounded,
                    color: context.primaryColor.withOpacity(0.6),
                    size: 20,
                  ),
                ],
              ),
              const SizedBox(height: 24),

              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: [
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        _buildInfoRow(
                          context,
                          label: ConstantString().hour,
                          value: appointmentTime,
                        ),
                        const SizedBox(height: 16),
                        _buildInfoRow(
                          context,
                          label: ConstantString().policlinic,
                          value: departmentName,
                        ),
                      ],
                    ),
                  ),

                  const SizedBox(width: 32),

                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        _buildInfoRow(
                          context,
                          label: ConstantString().doctor,
                          value: doctorName,
                        ),
                        const SizedBox(height: 16),
                      ],
                    ),
                  ),
                ],
              ),
              SizedBox(height: MediaQuery.of(context).size.height * 0.015),
              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: [
                  // İptal butonu
                  SizedBox(
                    height: 40,
                    child: OutlinedButton(
                      onPressed: onCancel,
                      style: OutlinedButton.styleFrom(
                        foregroundColor: ConstColor.red,
                        side: const BorderSide(
                          color: ConstColor.red,
                          width: 1.5,
                        ),
                        padding: const EdgeInsets.symmetric(
                          horizontal: 16,
                          vertical: 8,
                        ),
                      ),
                      child: Row(
                        mainAxisSize: MainAxisSize.min,
                        children: [
                          const Icon(
                            Icons.cancel_outlined,
                            color: ConstColor.red,
                            size: 18,
                          ),
                          const SizedBox(width: 8),
                          Text(
                            "İptal Et",
                            style: context.errorText.copyWith(
                              fontSize: 16,
                              fontWeight: FontWeight.w600,
                            ),
                          ),
                        ],
                      ),
                    ),
                  ),
                  isRegisterable
                      ? SizedBox(
                          height: 40,
                          child: ElevatedButton(
                            onPressed: onTap,
                            style: ElevatedButton.styleFrom(
                              padding: const EdgeInsets.symmetric(
                                horizontal: 16,
                                vertical: 8,
                              ),
                            ),
                            child: Row(
                              mainAxisSize: MainAxisSize.min,
                              children: [
                                Text(
                                  ConstantString().select,
                                  style: context.whiteButtonText.copyWith(
                                    fontSize: 16,
                                    fontWeight: FontWeight.w600,
                                  ),
                                ),
                                const SizedBox(width: 8),
                                const Icon(
                                  Icons.arrow_forward,
                                  color: ConstColor.white,
                                  size: 18,
                                ),
                              ],
                            ),
                          ),
                        )
                      : SizedBox(),
                ],
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildInfoRow(
    BuildContext context, {
    required String label,
    required String value,
  }) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          label,
          style: context.bodySecondary.copyWith(
            fontSize: 14,
            fontWeight: FontWeight.w500,
          ),
        ),
        const SizedBox(height: 4),
        Text(
          value,
          style: context.primaryText.copyWith(
            fontSize: 18,
            fontWeight: FontWeight.w600,
          ),
        ),
      ],
    );
  }
}
